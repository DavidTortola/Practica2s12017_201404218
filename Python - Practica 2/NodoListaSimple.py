#Clase nodo para la lista simple, con valor y siguiente.
class NodoListaSimple():

	def __init__(self):
		self.valor=None
		self.siguiente=None

	def getValor(self):
		return siguiente

	def setValor(self, valorAux):
		self.valor=valorAux

	def getSiguiente(self):
		return siguiente

	def setSiguiente(self, valorAux):
		self.siguiente=valorAux
